    public void populateInsuranceCompanyTable()
    {
     DefaultTableModel model = (DefaultTableModel) jTblNGOs.getModel();
        model.setRowCount(0);
        for(WorkRequest w:account.getClaimMoney().getWorkRequestList()){
            Object[] row = new Object[3];
            row[0] = w.getReceiver();
            row[1] = w.getMessage();
            row[2]=w.getStatus();
            model.addRow(row);
    }   
    }
