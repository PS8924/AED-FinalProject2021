    private void btnFundClaimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFundClaimActionPerformed
        // TODO add your handling code here:
        Organizations org =null;
        for(Network network :system.getNetworkList()){
            for(Enterprise e:network.getEp().getEnterpriselist()){
                for(Organizations o:e.getOrganizationdir().getOrganizationList()){
                    if(network.equals(lc)){
                        if(e.equals(jComboNGOCompany.getSelectedItem())){
                            if(o instanceof NGOVolunteer)
                            org=o;
                            break;
                        }
                    }
                }
            }
        }
        if(org !=null){
            PatientRequestsInsuranceCompany request = new PatientRequestsInsuranceCompany();
            request.setSender(account);
            request.setMessage(String.valueOf(cost));
            request.setStatus("Sent");
            org.getClaimMoney().getWorkRequestList().add(request);
            account.getClaimMoney().getWorkRequestList().add(request);
            JOptionPane.showMessageDialog(null,"fund claim sent to NGO !");
            try{
            Email emailConfig= new Email();
            Properties smtpProperties = emailConfig.loadProperties();
            String email = account.getUsername();
            emailConfig.sendEmail(smtpProperties, email, "Request Initiated", "Your request has being sent to the NGO!");
        }
            catch(Exception ex){
                
            }
            populateInsuranceCompanyTable();
         
    }//GEN-LAST:event_btnFundClaimActionPerformed
    if(org==null){
       JOptionPane.showMessageDialog(null,"No NGO Volunteer Organization created !");
    }
    }
