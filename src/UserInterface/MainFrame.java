    private void LoginBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginBtnActionPerformed
        // TODO add your handling code here:
        
        String username =UserNameTxtField.getText();
        char[] passwordCharArray= pwdTxtField.getPassword();
        String password = String.valueOf(passwordCharArray);
        UserAccount ua =system.getUserAccountDirectory().authenticateUser(username, password);
        Enterprise enterprise =null;
        Organizations org =null;
        if(ua==null)
        {
            for(Network network :system.getNetworkList())
            {
                for(Enterprise e:network.getEp().getEnterpriselist())
                {
                    System.out.println("Hey11");
                   ua=e.getUserAccountDirectory().authenticateUser(username, password);
                     System.out.println("Hey11");
                   if(ua==null)
                   {
                       for(Organizations o:e.getOrganizationdir().getOrganizationList())
                       {
                           ua=o.getUserAccountDirectory().authenticateUser(username, password);
                           if(ua!=null)
                           {
                               enterprise=e;
                               org=o;
                               break;
                           }
                       }
                   }
                   else 
                    {
                        enterprise=e;
                        break;
                    }
                if(org!=null)
                break;
                }
                if(enterprise!=null)
                    break;
            
            }
            
            
        }
        if(ua==null)
        {
            JOptionPane.showMessageDialog(null,"Invalid Credentials");
            return;
        }
        else 
        {
            
            Logout.setEnabled(true);
            LoginBtn.setEnabled(false);
            UserNameTxtField.setEnabled(false);
            pwdTxtField.setEnabled(false);
            CardLayout layout=(CardLayout)RightPanel.getLayout();
            RightPanel.add("workArea", ua.getRole().createWorkArea(RightPanel, ua, org, enterprise, system));
            layout.next(RightPanel);
        }
    }//GEN-LAST:event_LoginBtnActionPerformed

