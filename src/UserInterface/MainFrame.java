    private void forgotPwdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_forgotPwdActionPerformed
        // TODO add your handling code here:
        UserAccount acc =new UserAccount();
        int found =0;
        if(UserNameTxtField.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"UserName Field Is empty !");
            return;
        }
        else{
            for(Network network :system.getNetworkList()){
                for(Enterprise e:network.getEp().getEnterpriselist()){
                    for(Organizations o:e.getOrganizationdir().getOrganizationList()){
                        for(UserAccount ua :o.getUserAccountDirectory().getUserAccounts()){
                            if(ua.getUsername().equals(UserNameTxtField.getText())){

                                found =1;
                                acc=ua;

                            }
                        }
                    }
                }
            }
            if(found==1){
                ForgotPwd fpwd= new ForgotPwd(RightPanel,UserNameTxtField.getText(),system);
                CardLayout cardLayout=(CardLayout)RightPanel.getLayout();
                RightPanel.add(fpwd);
                cardLayout.next(RightPanel);
            }
            else
            JOptionPane.showMessageDialog(null,"Invalid UserName !");
    }//GEN-LAST:event_forgotPwdActionPerformed
    }    
    
    
