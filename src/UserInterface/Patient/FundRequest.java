    private void btnRequestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRequestActionPerformed
        // TODO add your handling code here:
        if(comboRequiredBloodType.getSelectedItem().equals("--Select--")){
            JOptionPane.showMessageDialog(null,"Please select a blood group !");
            return;
            
        }
        
        else{
        Organizations org =null;
        for(Network network :system.getNetworkList()){
            for(Enterprise e:network.getEp().getEnterpriselist()){
                for(Organizations o:e.getOrganizationdir().getOrganizationList()){
                    if(network.equals(lc)){
                        if(e.equals(TC)){
                            if(o instanceof DoctorOrganization)
                                org=o;
                            break;
                        }
                    }
                }
            }
        }
        if(org !=null){
            
            String message = (String)comboRequiredBloodType.getSelectedItem();
            PatientCostWorkRequest request= new PatientCostWorkRequest();
            request.setMessage(message);
            request.setSender(account);
       //     request.setMessage("whats the cost ?");
            request.setStatus("Sent");
            org.getCostEstimation().getWorkRequestList().add(request);
            account.getCq().getWorkRequestList().add(request);
            JOptionPane.showMessageDialog(null,"fund estimation sent !");
            populateTF();
            try{
            Email emailConfig= new Email();
            Properties smtpProperties = emailConfig.loadProperties();
            String email = account.getUsername();
            emailConfig.sendEmail(smtpProperties, email, "Request Initiated", "Your request has being sent to the Hospital");
        }
            catch(Exception ex){
                
            }
    }//GEN-LAST:event_btnRequestActionPerformed
        if(org ==null)
        {
            JOptionPane.showMessageDialog(null,"There was no Doctors Organization in the Hospital you have Selected !");
            return;
        }
        }
    
    }
