    public void populateTF()
    {
        DefaultTableModel model = (DefaultTableModel) jTblFundRequest.getModel();
        model.setRowCount(0);
        for(WorkRequest w:account.getCq().getWorkRequestList()){
            Object[] row = new Object[4];
            row[0] = w.getMessage();
            row[1] = w.getReceiver();
            row[2] = w.getStatus();
            String result = ((PatientCostWorkRequest) w).getTreatmentCost();
            row[3] = result == null ? "Waiting" : result; 
            if(result==null){
                btnClaim.setEnabled(false);
            }
            else
            btnClaim.setEnabled(true);
       //     row[0] = w.getStatus();
       //     row[1] = w.getMessage();
            model.addRow(row);
        }
        
        }
