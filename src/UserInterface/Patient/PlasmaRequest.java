    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        if(((String)Organ.getSelectedItem()).equalsIgnoreCase("--Select--"))
        {
            JOptionPane.showMessageDialog(null,"Please select the blood group you want");
            return;
        }
        else if(txtDiseasename.getText().equals("") ||
                txtDiseasDuration.getText().equals("") ||
                txtSeverity.getText().equals("") ||
                txtTreatmentDetails.getText().equals("")){
                        JOptionPane.showMessageDialog(null,"Please fill all you medical details!!");
                        return;
        }
        else{

            String message = (String)Organ.getSelectedItem();
            String DeseaseName = this.txtDiseasename.getText();
            String DeseaseTime = this.txtDiseasDuration.getText();
            String Sev =  txtSeverity.getText();
            String treatmentDetails = txtTreatmentDetails.getText();
            PatientOrganWorkRequest request = new PatientOrganWorkRequest();
            request.setMessage(message);
            request.setSender(account);
            request.setStatus("Sent"); 
            request.setRole("patient");
            request.setDesease(DeseaseName);
            request.setDeseasetime(DeseaseTime);
            request.setComplexity(Sev);
            request.setTeatmentdetails(treatmentDetails);
        Organizations org = null;
        for(Network network: system.getNetworkList())
         {
             for(Enterprise enterprise: network.getEp().getEnterpriselist())
             {
                for (Organizations organization : enterprise.getOrganizationdir().getOrganizationList()){
                    if(network.equals(jComboLocation.getSelectedItem())){
                        if(enterprise.equals(jComboHosp.getSelectedItem())){
                            if (organization instanceof DoctorOrganization){
                                org = organization;
                                break;
                            }
                        }
                     }
                 }
           }
        }
        if (org!=null)
        {
            org.getWorkQueue().getWorkRequestList().add(request);
            account.getWorkQueue().getWorkRequestList().add(request);
            JOptionPane.showMessageDialog(null,"Your request has been initiated");
            populatePRequestTable();
            try{
            Email emailConfig= new Email();
            Properties smtpProperties = emailConfig.loadProperties();
            String email = account.getUsername();
            emailConfig.sendEmail(smtpProperties, email, "Request Initiated", "Your request has being sent to the Hospital");
        }
            catch(Exception ex){
                
            }
        }
        if(org==null)
        {
            JOptionPane.showMessageDialog(null,"There was no Doctors Organization in the Hospital  you have Selected !");
            return;
        }
            
        }
        
    }//GEN-LAST:event_btnSubmitActionPerformed

